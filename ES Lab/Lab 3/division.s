	AREA	RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD	0x10001000	;SP
	DCD Reset_Handler

	AREA	division, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR	R0, =NUM1
	LDR	R1, [R0]
	LDR	R0, =NUM2
	LDR R2, [R0]
	MOV	R3, #0x00
UP	
	CMP	R1, R2
	BCC	DOWN
	SUB	R1, R2
	ADD	R3, #1
	B	UP
DOWN
	LDR	R0, =QUO
	STR	R3, [R0]	;R3 = (int) NUM1/NUM2
	LDR	R0, =REM
	STR	R1, [R0]
STOP
	B	STOP
	ALIGN
NUM1
	DCD	0x89ABCDEF
NUM2
	DCD	0x01234567
	
	AREA	data, DATA, READWRITE
QUO
	DCD 0x00
REM
	DCD	0x00
	END
